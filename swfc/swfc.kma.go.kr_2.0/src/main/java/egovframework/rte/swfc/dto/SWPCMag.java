package egovframework.rte.swfc.dto;

public class SWPCMag extends ChartData {

	Integer s;
	Double br;
	Double bt_l;
	Double bn;
	Double bt_s;
	Double lat;
	Double lon;
	
	public Integer getS() {
		return s;
	}
	public void setS(Integer s) {
		this.s = s;
	}
	public Double getBr() {
		return br;
	}
	public void setBr(Double br) {
		this.br = br;
	}
	public Double getBt_l() {
		return bt_l;
	}
	public void setBt_l(Double bt_l) {
		this.bt_l = bt_l;
	}
	public Double getBn() {
		return bn;
	}
	public void setBn(Double bn) {
		this.bn = bn;
	}
	public Double getBt_s() {
		return bt_s;
	}
	public void setBt_s(Double bt_s) {
		this.bt_s = bt_s;
	}
	public Double getLat() {
		return lat;
	}
	public void setLat(Double lat) {
		this.lat = lat;
	}
	public Double getLon() {
		return lon;
	}
	public void setLon(Double lon) {
		this.lon = lon;
	}
}
